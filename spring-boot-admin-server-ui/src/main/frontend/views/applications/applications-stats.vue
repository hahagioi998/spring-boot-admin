<template>
  <div class="inline-flex items-center">
    <div class="mr-1">
      <sba-tag :label="$t('applications.applications')" :value="applicationsCount" />
      <sba-tag :label="$t('applications.instances')" :value="instancesCount" />
    </div>
  </div>
</template>
<script>
  import SbaTag from '@/components/sba-tag.vue';
  export default {
    components: { SbaTag},
    props: {
      applications: {
        type: Array,
        default: () => [],
      }
    },
    computed: {
      applicationsCount() {
        return this.applications.length;
      },
      instancesCount() {
        return this.applications.reduce((current, next) => current + next.instances.length, 0);
      },
      downCount() {
        return this.applications.reduce((current, next) => {
          return current + (next.instances.filter(instance => instance.statusInfo.status !== 'UP').length);
        }, 0);
      }
    }
  }
</script>
