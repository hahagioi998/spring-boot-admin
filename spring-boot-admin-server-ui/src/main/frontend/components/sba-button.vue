<template>
  <button
    :class="classNames"
    :disabled="disabled===true"
    class="btn relative"
    @click="$emit('click', $event)"
  >
    <slot />
  </button>
</template>

<script>
export default {
  name: 'SbaButton',
  props: {
    flat: {
      type: Boolean,
      default: false
    },
    size: {
      type: String,
      default: 'sm'
    },
    disabled: {
      type: Boolean,
      default: false
    },
  },
  emits: ['click'],
  computed: {
    classNames() {
      return {
        'shadow-sm': !this.flat,
        // Sizes
        'px-1 py-0': this.size === 'xs',
        'px-3 py-2': this.size === 'sm',
        'px-5 py-2.5': this.size === ''
        // Types
      };
    }
  }
}
</script>

